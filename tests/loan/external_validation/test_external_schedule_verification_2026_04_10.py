    principal = Money(principal)
    interest_rate = InterestRate(rate, precision=6)
    disbursement_date = datetime.strptime(disbursement_date, "%Y-%m-%d")
    loan = Loan(
        principal,
        interest_rate,
        disbursement_date=disbursement_date,
        due_dates=[datetime.strptime(d, "%Y-%m-%d").date() for d in due_dates],
    )
    schedule = loan.get_original_schedule()
    assert abs(schedule[0].payment_amount.real_amount - Decimal(expected)) <= ONE_CENT
